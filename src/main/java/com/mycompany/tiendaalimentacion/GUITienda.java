    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
    // Verificar si hay un ítem seleccionado
        if (jComboBox1.getSelectedItem() == null) {
            return;
        }
        String idSeleccionado = (String) jComboBox1.getSelectedItem(); // Obtener el ID seleccionado
        String sql = "SELECT Articulo, descripcion, stock, precio FROM articulos WHERE Id_producto = ?";
        // Try-with-resources para asegurar cierre automático de recursos
        try (Connection conn = new ConexionBD().conectar();
             PreparedStatement pst = conn.prepareStatement(sql)) {
             pst.setString(1, idSeleccionado); // Pasar el parámetro a la consulta
            try (ResultSet rs = pst.executeQuery()) {
                if (rs.next()) {
                    // Asignar los datos recuperados a los componentes de la interfaz
                    jTextField1.setText(rs.getString("Articulo"));
                    jTextArea1.setText(rs.getString("descripcion"));
                    jTextField2.setText(String.valueOf(rs.getInt("stock")));
                    jTextField3.setText(String.format("%.2f €", rs.getDouble("precio")));
                } else {
                    // Mostrar advertencia si no se encuentra el artículo
                    JOptionPane.showMessageDialog(this,
                        "No se encontró el artículo seleccionado",
                        "Artículo no encontrado",
                        JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (SQLException | ClassNotFoundException ex) {
            // Manejar errores de conexión o consulta
            Logger.getLogger(GUITienda.class.getName()).log(Level.SEVERE, "Error al cargar artículo", ex);
            JOptionPane.showMessageDialog(this,
                "Error al cargar los datos del artículo: " + ex.getMessage(),
                "Error de base de datos",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

