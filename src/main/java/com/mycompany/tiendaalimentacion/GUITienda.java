    /**
     * Método para cargar los IDs de los productos en el ComboBox
     */
    public void ComboBoxID(JComboBox comboID) throws ClassNotFoundException {
        ConexionBD con = new ConexionBD(); // Crear instancia de conexión
        con.conectar(); // Conectar a la base de datos
        String sql = "select * from articulos"; // Consulta SQL para traer todos los artículos

        Statement st;
        try {
            st = con.conectar().createStatement(); // Crear un Statement para ejecutar la consulta
            ResultSet rs = st.executeQuery(sql); // Ejecutar la consulta y obtener resultados
            jComboBox1.removeAllItems(); // Limpiar ComboBox antes de llenarlo
            while(rs.next()) {
              jComboBox1.addItem(rs.getString("Id_producto")); // Añadir cada ID al ComboBox
            }
        } catch (SQLException e) {
            // Mostrar mensaje de error si falla la consulta
            JOptionPane.showMessageDialog(null, "Error al mostrar:,ERROR: " + e.toString());
        }
    }

